#ifndef LLVM_LIB_TARGET_PROL16_INSTPRINTER_PROL16INSTPRINTER_H
#define LLVM_LIB_TARGET_PROL16_INSTPRINTER_PROL16INSTPRINTER_H

#include "llvm/ADT/StringRef.h"

#include "llvm/MC/MCInstPrinter.h"
#include "llvm/MC/MCAsmInfo.h"
#include "llvm/MC/MCInstrInfo.h"
#include "llvm/MC/MCRegisterInfo.h"
#include "llvm/MC/MCInst.h"
#include "llvm/MC/MCSubtargetInfo.h"

#include "llvm/Support/raw_ostream.h"

namespace llvm {

class PROL16InstPrinter final : public MCInstPrinter {
public:
	PROL16InstPrinter(MCAsmInfo const &mcAsmInfo, MCInstrInfo const &mcInstrInfo, MCRegisterInfo const &mcRegisterInfo);

	/// Print the specified MCInst to the specified raw_ostream.
	void printInst(MCInst const *MI, raw_ostream &OS, StringRef Annot, MCSubtargetInfo const &STI) override;

	void printRegister(unsigned const reg, raw_ostream &rawOstream) const;

	/**** methods auto-generated by tblgen ****/
	void printInstruction(MCInst const *MI, raw_ostream &O);
	static char const* getRegisterName(unsigned RegNo);

	/**** methods needed by the auto-generated methods ****/
	void printOperand(MCInst const *mcInst, unsigned operandNumber, raw_ostream &rawOstream);
};

} // end namespace llvm

#endif
